    <div>
        H&atilde;ng sản xuất đồng hồ đầu ti&ecirc;n sử dụng loại Micro-Roto n&agrave;y l&agrave; Buren với chiếc Slender được ra mắt v&agrave;o năm 1954.</div>
    <h3>
        4. Peripheral Rotor</h3>
    <div>
        Kh&aacute;c biệt gần như ho&agrave;n to&agrave;n với c&aacute;c cấu tr&uacute;c kể tr&acirc;n, Peripheral Rotor thường được bố tr&iacute; nằm ở r&igrave;a m&aacute;y. N&oacute; thường c&oacute; cấu tạo bao gồm một v&ograve;ng kim loại với c&aacute;c b&aacute;nh răng được kết nối với bộ phận nạp c&oacute;t v&agrave;o ổ trữ c&oacute;t. Tr&ecirc;n một nửa v&ograve;ng quay đ&oacute; sẽ được gắn một quả nặng được l&agrave;m bằng c&aacute;c thanh kim loại qu&yacute; như V&agrave;ng, Platinum v&agrave; Tungsten, với cấu tr&uacute;c như một h&igrave;nh trụ vu&ocirc;ng uốn cong theo bề mặt v&ograve;ng kim loại.</div>
