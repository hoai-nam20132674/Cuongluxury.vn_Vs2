    <h3>
        5. Bumper Rotor</h3>
    <div>
        Ngo&agrave;i 4 thiết kế Rotor phổ biến, ch&uacute;ng ta c&ograve;n c&oacute; Bumper Rotor, l&agrave; một loại Full Rotor cổ xưa được ra mắt v&agrave;o năm 1922, v&agrave; từng phổ biến tr&ecirc;n c&aacute;c d&ograve;ng m&aacute;y cơ học cổ điển. N&oacute; c&oacute; thiết kế tương đối độc đ&aacute;o với h&igrave;nh d&aacute;ng tựa như một lưỡi liềm được gắn với trung t&acirc;m bộ m&aacute;y. Tr&ecirc;n đường di chuyển của n&oacute; sẽ được th&ecirc;m gắn một cấu trục đặc biệt với 2 chiếc l&ograve; xo xoắn 2 b&ecirc;n.&nbsp;</div>
