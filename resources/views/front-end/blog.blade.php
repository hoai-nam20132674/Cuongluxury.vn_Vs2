    <div>
        &nbsp;</div>
    <div>
        Breguet l&agrave; thương hiệu điển h&igrave;nh thường hay sử dụng cấu tr&uacute;c Off-center Rotor cho những thiết kế đồng hồ v&agrave; bộ m&aacute;y cơ học của m&igrave;nh.</div>
    <h3>
        3. Micro-Rotor:</h3>
    <div>
        C&oacute; thiết kế c&ograve;n nhỏ hơn cả Off-center Rotor, Micro-Rotor thương được đặt v&agrave;o phần r&igrave;a th&acirc;n m&aacute;y thay v&igrave; ch&iacute;nh giữa bộ m&aacute;y như c&aacute;c mẫu Full Rotor. Điều n&agrave;y khiến n&oacute; nằm gọn b&ecirc;n trong bộ m&aacute;y đồng hồ v&agrave; gi&uacute;p giảm thiểu đ&aacute;ng kể độ d&agrave;y của bộ m&aacute;y, đồng thời n&oacute; cũng gi&uacute;p đồng hồ dễ d&agrave;ng ph&ocirc; diễn được vẻ đẹp tinh xảo của c&aacute;c chi tiết b&ecirc;n trong bộ m&aacute;y&nbsp;</div>
