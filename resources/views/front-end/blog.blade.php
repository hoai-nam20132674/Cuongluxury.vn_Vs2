        &nbsp;</div>
    <div>
        <img alt="" src="https://cuongluxury.vn/uploads/images/images/m/Rotor-l%C3%A0-g%C3%AC-4%20ki%E1%BB%83u-Rotor-th%C6%B0%E1%BB%9Dng-g%E1%BA%B7p-tr%C3%AAn-%C4%91%E1%BB%93ng-h%E1%BB%93-c%C6%A1-kh%C3%AD-%20(5).jpg" style="width: 1222px; height: 812px;" /></div>
    <div>
        &nbsp;</div>
    <div>
        Tuy nhi&ecirc;n, kết cấu nhỏ gọn n&agrave;y cũng đem đến cho Micro-Rotor một nhược điểm, đ&oacute; l&agrave; kh&ocirc;ng nhạy cảm với c&aacute;c chuyển động như Full Rotor. Do đ&oacute;, n&oacute; bắt buộc phải được l&agrave;m bằng c&aacute;c kim loại nặng như V&agrave;ng, Platinum, Tungsten. Đ&acirc;y l&agrave; những kim loại qu&yacute; hiếm v&agrave; c&oacute; gi&aacute; th&agrave;nh cao, việc sử dụng n&oacute; l&agrave;m tăng chi ph&iacute; sản xuất đồng hồ n&ecirc;n chủ yếu thường chỉ được sử dụng bởi c&aacute;c thương hiệu chế t&aacute;c xa xỉ.</div>
