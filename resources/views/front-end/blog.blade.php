        Cơ chế quay l&ecirc;n c&oacute;t của Peripheral Rotor cũng kh&aacute; tương đồng với Full Rotor. Tuy nhi&ecirc;n, do được thiết kế để nằm liền với r&igrave;a ngo&agrave;i của bộ m&aacute;y, n&ecirc;n n&oacute; cũng gi&uacute;p bộ m&aacute;y của đồng hồ mỏng đi tương đối. Đổi lại việc chế tạo ra cấu tr&uacute;c n&agrave;y đ&ograve;i hỏi sự phức tạp hơn kh&aacute; nhiều so với c&aacute;c d&ograve;ng Rotor kh&aacute;c.</div>
