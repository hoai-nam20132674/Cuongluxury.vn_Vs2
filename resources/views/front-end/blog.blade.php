                                            <div>
        Rotor hay củ văng hoặc quả nặng, l&agrave; một th&agrave;nh phần đặc biệt quan trọng của bộ m&aacute;y cơ học tự l&ecirc;n d&acirc;y c&oacute;t, với mục đ&iacute;ch gi&uacute;p đồng hồ tự động l&ecirc;n c&oacute;t th&ocirc;ng qua c&aacute;c chuyển động bằng tay của người sử dụng.&nbsp;</div>
    <div>
        &nbsp;</div>
    <div>
        Rotor thường được gắn trực tiếp v&agrave;o bộ m&aacute;y của đồng hồ v&agrave; li&ecirc;n kết chặt chẽ với bộ trữ c&oacute;t. N&oacute; c&oacute; cấu tr&uacute;c h&igrave;nh b&aacute;n nguyệt với nhiều h&igrave;nh dạng v&agrave; thiết kế kh&aacute;c nhau nhưng thường được l&agrave;m từ c&aacute;c kim loại nặng. Khi xoay cổ tay, c&aacute;c thao t&aacute;c chuyển động sẽ tạo động năng để l&agrave;m xoay chiếc Rotor. Rotor xoay tr&ograve;n xung quanh trục sẽ k&eacute;o căng bộ c&oacute;t th&ocirc;ng qua cấu tr&uacute;c b&aacute;nh răng li&ecirc;n kết.</div>
    <div>
        &nbsp;</div>
    <div>
