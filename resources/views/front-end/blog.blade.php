    <div>
        &nbsp;</div>
    <div>
        Th&ocirc;ng thường nếu sử dụng đồng hồ thường xuy&ecirc;n khoảng 8 tiếng mỗi ng&agrave;y với nhiều hoạt động đ&ograve;i hỏi cổ tay phải chuyển động, bạn sẽ cần &iacute;t thời gian hơn để l&ecirc;n c&oacute;t cho đồng hồ v&igrave; Rotor đ&atilde; gi&uacute;p bạn tận dụng tối đa c&aacute;c chuyển động h&agrave;ng ng&agrave;y để l&ecirc;n c&oacute;t tự động cho bộ m&aacute;y.</div>
    <div>
        &nbsp;</div>
    <div>
        Sau nhiều năm ph&aacute;t triển, cho đến nay,&nbsp; ch&uacute;ng ta c&oacute; thể bắt gặp đến 4 kiểu Rotor phổ biến thường được trang bị tr&ecirc;n c&aacute;c d&ograve;ng đồng hồ cơ tự động, bao gồm Full rotor,&nbsp; Off-center Rotor, Micro-Rotor v&agrave; Peripheral Rotor. Cụ thể:</div>
    <h3>
        1. Full rotor:&nbsp;</h3>
    <div>
        Full Rotor l&agrave; loại Rotor phổ biến nhất tr&ecirc;n c&aacute;c mẫu đồng hồ cơ học, được mọi thương hiệu từ phổ biến cho đến cao cấp v&agrave; xa xỉ sử dụng trong c&aacute;c mẫu đồng hồ của m&igrave;nh. N&oacute; được thiết kế với k&iacute;ch thước lớn - tương ứng với b&aacute;n k&iacute;nh bộ m&aacute;y đồng hồ với phần trục được đặt nằm ở ch&iacute;nh giữa bộ m&aacute;y.</div>
    <div>
        &nbsp;</div>
    <div>
        <img alt="" src="https://cuongluxury.vn/uploads/images/images/m/Rotor-l%C3%A0-g%C3%AC-4%20ki%E1%BB%83u-Rotor-th%C6%B0%E1%BB%9Dng-g%E1%BA%B7p-tr%C3%AAn-%C4%91%E1%BB%93ng-h%E1%BB%93-c%C6%A1-kh%C3%AD-%20(9).jpg" style="width: 1222px; height: 812px;" /></div>
    <div>
        &nbsp;</div>
    <div>
        Loại Rotor n&agrave;y thường c&oacute; khả năng nạp c&oacute;t nhanh nhất, nhưng đi k&egrave;m với đ&oacute; l&agrave;&nbsp; 2 nhược điểm nhỏ, đ&oacute; l&agrave;:</div>
    <ul>
        <li>
            Thường khiến cấu tr&uacute;c của bộ m&aacute;y d&agrave;y hơn đ&aacute;ng kể&nbsp;</li>
        <li>
            V&agrave; l&agrave;m che che đi mất một phần của mặt đ&aacute;y m&aacute;y.</li>
    </ul>
    <div>
        &nbsp;</div>
    <div>
        Nhiều thương hiệu để hạn chế những nhược điểm tr&ecirc;n đ&atilde; tạo ra những chiếc Full Rotor được kho&eacute;t rỗng ở giữa nhằm mở rộng c&aacute;c phần c&oacute; thể nh&igrave;n thấy của bộ m&aacute;y, hoặc được trang tr&iacute; bằng c&aacute;c họa tiết đặc biệt t&ugrave;y theo sức s&aacute;ng tạo của mỗi thương hiệu.&nbsp;</div>
    <div>
        &nbsp;</div>
    <div>
        <img alt="" src="https://cuongluxury.vn/uploads/images/images/m/Rotor-l%C3%A0-g%C3%AC-4%20ki%E1%BB%83u-Rotor-th%C6%B0%E1%BB%9Dng-g%E1%BA%B7p-tr%C3%AAn-%C4%91%E1%BB%93ng-h%E1%BB%93-c%C6%A1-kh%C3%AD-%20(7).jpg" style="width: 1222px; height: 812px;" /></div>
    <div>
        &nbsp;</div>
    <div>
        V&iacute; dụ như Patek Philippe sẽ c&oacute; những chiếc Rotor đặc được tạo h&igrave;nh lưỡi r&igrave;u v&agrave; trang tr&iacute; bởi c&aacute;c v&acirc;n sọc Cotes De Geneve, hay như những phi&ecirc;n bản đồng hồ kỷ niệm 50 năm của Audemars Piguet được trang bị 1 chiếc Full Rotor rỗng ruột với biểu tượng 50th được đi&ecirc;u khắc ở ch&iacute;nh giữa</div>
    <div>
        &nbsp;</div>
    <div>
        H&atilde;ng sản xuất đồng hồ đầu ti&ecirc;n cho ra mắt 1 bộ m&aacute;y full rotor với khả năng xoay 360 độ l&agrave; Rolex với phi&ecirc;n bản Oyster Perpetual được ra mắt v&agrave;o năm 1931.&nbsp;</div>
    <h3>
        2. Off-center Rotor</h3>
    <div>
        Vẫn l&agrave; một phi&ecirc;n bản của Full Rotor nhưng được thu nhỏ lại một ch&uacute;t về mặt k&iacute;ch thước v&agrave; đặt lệch t&acirc;m thay v&igrave; đặt ở ch&iacute;nh giữa bộ m&aacute;y. Về cơ bản Off-center Rotor v&agrave; Full Rotor kh&ocirc;ng c&oacute; nhiều điểm kh&aacute;c biệt về mặt thiết kế ngoại trừ k&iacute;ch thước nhỏ hơn v&agrave; vị tr&iacute; đặt lệch.</div>
